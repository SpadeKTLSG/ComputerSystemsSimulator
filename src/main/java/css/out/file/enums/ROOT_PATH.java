package css.out.file.enums;

/**
 * 根目录下的子目录名
 * <p>|  home 用户子目录</p>
 * <p>|  app 应用程序目录</p>
 * <p>|  tmp 临时可变目录</p>
 * <p>|  conf 配置文件目录</p>
 * <p>|  mnt 设备挂载目录</p>
 * <p>|  bin 可执行命令目录</p>
 * <p>|  lib 系统库文件以及系统资料目录</p>
 * <p>|  boot 系统内核程序目录</p>
 */
public enum ROOT_PATH {

    //home 用户子目录
    //app 应用程序目录
    //tmp 临时可变目录
    //conf 配置文件目录
    //mnt 设备挂载目录
    //bin 可执行命令目录
    //lib 系统库文件以及系统资料目录
    //boot 系统内核程序目录
    home, app, tmp, conf, mnt, bin, lib, boot;

}
