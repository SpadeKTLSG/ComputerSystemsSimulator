package css.out.file.entity;

/**
 * 根目录下的八个目录对应的枚举常量:
 * home
 * app
 * tmp
 * conf
 * mnt
 * bin
 * lib
 * boot
 */
public enum ROOT {

    //home 用户子目录
    //app 应用程序目录
    //tmp 临时可变目录
    //conf 配置文件目录
    //mnt 设备挂载目录
    //bin 可执行命令目录
    //lib 系统库文件以及系统资料目录
    //boot 系统内核程序目录
    home, app, tmp, conf, mnt, bin, lib, boot;

}
